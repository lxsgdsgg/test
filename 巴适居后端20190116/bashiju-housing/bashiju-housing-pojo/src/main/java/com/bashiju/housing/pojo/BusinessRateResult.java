public class BusinessRateResult implements Serializable {
	private static final long serialVersionUID = 371284948830008282L;
	/**契税比例**/
	private Float deedTax;
	/**增值税比例**/
	private Float addedValueTax;
	/**个人所得税比例**/
	private Float incomeTax;
	public Float getDeedTax() {
		return deedTax;
	}
	public void setDeedTax(Float deedTax) {
		this.deedTax = deedTax;
	}
	public Float getAddedValueTax() {
		return addedValueTax;
	}
	public void setAddedValueTax(Float addedValueTax) {
		this.addedValueTax = addedValueTax;
	}
	public Float getIncomeTax() {
		return incomeTax;
	}
	public void setIncomeTax(Float incomeTax) {
		this.incomeTax = incomeTax;
	}
	@Override
	public String toString() {
		return "BusinessRateResult [deedTax=" + deedTax + ", addedValueTax=" + addedValueTax + ", incomeTax="
				+ incomeTax + "]";
	}
	
}
