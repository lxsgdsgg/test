package com.bashiju.manage.pojo;

public class DictionaryContent {
	private Long id;
	private String itemName;
	private Long dictionaryCatalogId;
	private String fieldName;
	private String description;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public Long getDictionaryCatalogId() {
		return dictionaryCatalogId;
	}
	public void setDictionaryCatalogId(Long dictionaryCatalogId) {
		this.dictionaryCatalogId = dictionaryCatalogId;
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
}
